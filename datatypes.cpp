#include<iostream>
using namespace std;

int main()
{
    //integer data types
    int apples=5;
    cout<<apples<<endl;
    cout<<"size of int:"<<sizeof(apples)<<endl;

    //char datatypes
    char sign='&';
    cout<<sign<<endl;
    cout<<"size of char:"<<sizeof(sign)<<endl;

    //bool datatypes
    bool flag=true;
    cout<<flag<<endl;
    cout<<"size of bool:"<<sizeof(flag)<<endl;

    //float datatypes
    float score = 3.14;
    cout<<score<<endl;
    cout<<"size of float:"<<sizeof(score)<<endl;

}